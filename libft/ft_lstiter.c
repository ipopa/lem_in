#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *tmp;

	if ((tmp = (t_list *)malloc(sizeof(t_list))) == NULL)
		return ;
	if (lst && f)
	{
		tmp = lst;
		while (tmp != NULL)
		{
			(*f)(tmp);
			tmp = tmp->next;
		}
	}
}
