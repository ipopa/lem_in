#include "libft.h"

char	*ft_strmap(char const *s, char (*f) (char))
{
	int		i;
	char	*map;

	i = 0;
	if (s == NULL || (f) == NULL)
		return (NULL);
	map = malloc((ft_strlen(s) + 1) * sizeof(char*));
	while (s[i] != '\0')
	{
		map[i] = f(s[i]);
		i++;
	}
	map[i] = '\0';
	return (map);
}
