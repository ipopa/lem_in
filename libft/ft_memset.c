#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*str;
	unsigned int	i;

	str = b;
	i = 0;
	while (i < len)
	{
		*str++ = c;
		i++;
	}
	return (b);
}
