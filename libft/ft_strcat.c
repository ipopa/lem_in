#include "libft.h"

char	*ft_strcat(char *s1, char *s2)
{
	int	c1;
	int	c2;

	c1 = 0;
	c2 = 0;
	while (s1[c1] != '\0')
		c1++;
	while (s2[c2] != '\0')
	{
		s1[c1] = s2[c2];
		c1++;
		c2++;
	}
	s1[c1] = '\0';
	return (s1);
}
