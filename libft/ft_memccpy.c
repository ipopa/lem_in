#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	char	*dstcpy;
	char	*srccpy;

	dstcpy = (char*)dst;
	srccpy = (char*)src;
	while (n-- > 0)
	{
		if ((*dstcpy++ = *srccpy++) == c)
			return (dstcpy);
	}
	return (NULL);
}
